  end

  def aura
    AURA_NAMES[self.aura_id]
  end

  def amplitude
    to_int_or_float(self[:amplitude])
  end

  def to_int_or_float(value)
    if value % 1000 > 0
      value / 1000.0
    else
      value / 1000
    end
  end
